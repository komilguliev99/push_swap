#include "libft.h"

t_list		*ft_lstnth(t_list *lst, int n)
{
	int		i;

	if (!lst)
		return (NULL);
	i = 0;
	while (i < n && lst)
	{
		lst = lst->next;
		i++;
	}
	return (lst);
}
