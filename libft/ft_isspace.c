#include "libft.h"

int		ft_isspace(int c)
{
	unsigned char	t;

	t = (unsigned char)c;
	if (t == ' ' || t == '\n' || t == '\v' || t == '\t'
			|| t == '\f' || t == '\r')
		return (1);
	return (0);
}
