#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	size_t	i;
	size_t	l;

	i = 0;
	l = ft_strlen(dst);
	while (src[i] != '\0')
	{
		dst[l + i] = src[i];
		i++;
	}
	dst[l + i] = '\0';
	return (dst);
}
