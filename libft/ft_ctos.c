#include "libft.h"

char		*ft_ctos(char c)
{
	char	*str;

	if (!(str = (char *)malloc(2)))
		return (NULL);
	if (c == '\0')
		str[0] = '\x00';
	else
		str[0] = c;
	str[1] = '\0';
	return (str);
}
