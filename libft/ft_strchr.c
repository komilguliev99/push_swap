#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	*s;

	s = (char *)str;
	while (*s != '\0' && *s != (char)c)
		s++;
	if (*s == (char)c)
		return (s);
	else
		return (NULL);
}
