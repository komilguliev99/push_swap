#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	char	*s;
	int		i;

	i = 0;
	s = (char *)src;
	str = (char *)malloc(ft_strlen(s) + 1);
	if (!str)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
